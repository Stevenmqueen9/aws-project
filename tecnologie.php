        <div class="section">
            <h2>AWS (Amazon Web Services)</h2>
            <p>AWS è una piattaforma di cloud computing offerta da Amazon. Fornisce una vasta gamma di servizi cloud, inclusi calcolo, storage, database, analisi, intelligenza artificiale, Internet of Things (IoT) e molto altro. Alcuni dei servizi più conosciuti di AWS includono:</p>
            <ul>
                <li><strong>EC2 (Elastic Compute Cloud)</strong>: Offre capacità di calcolo scalabili nel cloud.</li>
                <li><strong>S3 (Simple Storage Service)</strong>: Servizio di storage per l'archiviazione di grandi quantità di dati.</li>
                <li><strong>RDS (Relational Database Service)</strong>: Servizio di database relazionale gestito che supporta diversi motori di database come MySQL, PostgreSQL, MariaDB, Oracle e SQL Server.</li>
                <li><strong>Lambda</strong>: Permette di eseguire codice in risposta a eventi senza gestire server.</li>
            </ul>
        </div>
        <div class="section">
            <h2>Docker</h2>
            <p>Docker è una piattaforma che consente di automatizzare il deployment di applicazioni all'interno di container. I container sono pacchetti standardizzati che includono tutto il necessario per eseguire un'applicazione, compreso il codice, le librerie e le dipendenze. Le principali caratteristiche di Docker includono:</p>
            <ul>
                <li><strong>Portabilità</strong>: I container possono essere eseguiti su qualsiasi sistema che supporti Docker, rendendo il software portabile e consistente tra diversi ambienti.</li>
                <li><strong>Isolamento</strong>: Ogni container è isolato dagli altri, garantendo che le applicazioni non interferiscano tra loro.</li>
                <li><strong>Efficienza</strong>: I container condividono il kernel del sistema operativo, risultando più leggeri rispetto alle macchine virtuali.</li>
            </ul>
        </div>
        <div class="section">
            <h2>MySQL</h2>
            <p>MySQL è un sistema di gestione di database relazionali (RDBMS) open source. Utilizza il linguaggio SQL (Structured Query Language) per gestire e manipolare i dati. MySQL è molto popolare grazie alla sua velocità, affidabilità e facilità d'uso. È ampiamente utilizzato in applicazioni web e software open source come WordPress, Joomla e Drupal.</p>
        </div>
        <div class="section">
            <h2>phpMyAdmin</h2>
            <p>phpMyAdmin è uno strumento di amministrazione per MySQL scritto in PHP. Fornisce un'interfaccia web intuitiva per gestire i database MySQL, permettendo di eseguire operazioni comuni come:</p>
            <ul>
                <li>Creare e modificare database, tabelle, colonne, indici e utenti</li>
                <li>Eseguire query SQL</li>
                <li>Importare ed esportare dati</li>
                <li>Gestire i permessi degli utenti</li>
            </ul>
        </div>
        <div class="section">
            <h2>Apache</h2>
            <p>Apache HTTP Server, comunemente noto come Apache, è un server web open source. È uno dei server web più utilizzati al mondo. Apache supporta una vasta gamma di funzionalità tramite moduli, tra cui l'autenticazione, la riscrittura degli URL, la compressione, il supporto per diversi linguaggi di scripting e la sicurezza. Le sue caratteristiche principali includono:</p>
            <ul>
                <li><strong>Modularità</strong>: È possibile abilitare o disabilitare funzionalità tramite moduli.</li>
                <li><strong>Compatibilità</strong>: Supporta diversi sistemi operativi, inclusi Unix, Linux, Windows e macOS.</li>
                <li><strong>Affidabilità e Performance</strong>: È noto per la sua stabilità e le sue buone prestazioni sotto carichi elevati.</li>
            </ul>
